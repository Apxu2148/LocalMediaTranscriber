  function t(key, variables = {}) {
    let value = translations[language][key] || translations[DEFAULT_LANGUAGE][key] || key;
    for (const [name, replacement] of Object.entries(variables)) {
      value = value.replaceAll(`{${name}}`, String(replacement));
    }
    return value;
  }

  function translateText(value) {
    if (language !== "en" || !value) {
      return value;
    }
    let translated = String(value);
    for (const [source, replacement] of Object.entries(textPairs).sort(([left], [right]) => right.length - left.length)) {
      translated = translated.replaceAll(source, replacement);
    }
    return translated;
  }

